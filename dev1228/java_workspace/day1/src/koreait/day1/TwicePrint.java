package koreait.day1;

public class TwicePrint {

	public static void main(String[] args) {
		   
		 System.out.println("트와이스");
		 System.out.println("------------------");
		 System.out.println("사나\t1996.12.29");
		 System.out.println("지효\t1997.02.01");
		 System.out.println("미나\t1997.03.24");
		 System.out.printf("%-12s%s\n","다현","1998.05.28");
		 System.out.printf("%-12s%s\n","채영","1999.04.23");
		 System.out.printf("%-12s%s\n","쯔위","1996.06.14");
		}

    // 	 \n, \t : 특정기능문자(이스케이프 문자(시퀸스) \로 시작합니다.)
}

